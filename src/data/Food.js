class Food {
  constructor(
    id,
    name,
    calories,
    protein,
    carbs,
    fat,
    servingSize,
    servingUnit
  ) {
    this.id = id;
    this.name = name;
    this.calories = calories;
    this.protein = protein;
    this.carbs = carbs;
    this.fat = fat;
    this.servingSize = servingSize;
    this.servingUnit = servingUnit;
  }
}
